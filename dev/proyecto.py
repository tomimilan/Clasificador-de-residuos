"""Celda 3: Implementación de la Clase Dataset en PyTorch"""

import torch
from torch.utils.data import Dataset
from PIL import Image

class GarbageGIRSUDataset(Dataset):
    """Clase Dataset personalizada para la gestión de residuos bajo la Ley GIRSU Mendoza."""
    def __init__(self, csv_path, transform=None):
        self.data_frame = pd.read_csv(csv_path)
        self.transform = transform

    def __len__(self):
        return len(self.data_frame)

    def __getitem__(self, idx):
        img_path = self.data_frame.iloc[idx, 0]
        label = int(self.data_frame.iloc[idx, 1])

        # Carga robusta en modo RGB para mitigar problemas con imágenes en escala de grises
        image = Image.open(img_path).convert("RGB")

        if self.transform:
            image = self.transform(image)

        return image, label

"""Celda 4: Data Augmentation y Configuración de los DataLoaders"""

from torchvision import transforms
from torch.utils.data import DataLoader

IMG_SIZE = 224
BATCH_SIZE = 32

IMAGENET_MEAN = [0.485, 0.456, 0.406]
IMAGENET_STD = [0.229, 0.224, 0.225]

# Pipeline de Entrenamiento con Data Augmentation para producción
train_transforms = transforms.Compose([
    transforms.Resize((IMG_SIZE, IMG_SIZE)),
    transforms.RandomHorizontalFlip(p=0.5),
    transforms.RandomVerticalFlip(p=0.3),
    transforms.RandomRotation(degrees=30),
    transforms.ColorJitter(brightness=0.2, contrast=0.2, saturation=0.2),
    transforms.ToTensor(),
    transforms.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD)
])

# Pipeline de Validación y Test (Sin aumentación)
val_test_transforms = transforms.Compose([
    transforms.Resize((IMG_SIZE, IMG_SIZE)),
    transforms.ToTensor(),
    transforms.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD)
])

# Instanciación
train_dataset = GarbageGIRSUDataset("data/train.csv", transform=train_transforms)
val_dataset   = GarbageGIRSUDataset("data/val.csv", transform=val_test_transforms)
test_dataset  = GarbageGIRSUDataset("data/test.csv", transform=val_test_transforms)

train_loader = DataLoader(train_dataset, batch_size=BATCH_SIZE, shuffle=True, drop_last=True)
val_loader   = DataLoader(val_dataset, batch_size=BATCH_SIZE, shuffle=False)
test_loader  = DataLoader(test_dataset, batch_size=BATCH_SIZE, shuffle=False)


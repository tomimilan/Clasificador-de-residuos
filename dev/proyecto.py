# La función ahora toma el dataset ya creado y el pipeline de transformaciones
def mostrar_aumentaciones(dataset, transform_pipeline, num_imagenes=4, num_variaciones=3):
    """Muestra el efecto de data augmentation en varias imágenes del dataset.

    Args:
        dataset (torch.utils.data.Dataset): El dataset (ej. train_dataset) con imágenes y labels.
        transform_pipeline (torchvision.transforms.Compose): El pipeline de transformaciones a aplicar.
        num_imagenes (int): Número de imágenes distintas a mostrar.
        num_variaciones (int): Número de versiones aumentadas para cada imagen.
    """
    # Creamos una grilla: filas = imágenes distintas, columnas = variaciones de cada una
    fig, axs = plt.subplots(num_imagenes, num_variaciones, figsize=(15, 3 * num_imagenes))
    fig.suptitle("Data Augmentation: Versiones Aumentadas de Imágenes Distintas", fontsize=14, fontweight='bold')

    for row in range(num_imagenes):
        # Obtener la ruta de la imagen original del dataframe subyacente del dataset
        # Usamos .data_frame para acceder al pandas DataFrame dentro de GarbageGIRSUDataset
        img_path = dataset.data_frame.iloc[row, 0]
        raw_image = Image.open(img_path).convert("RGB")

        for col in range(num_variaciones):
            # Aplicamos el pipeline de transformaciones directamente a la imagen RAW
            # para generar múltiples aumentaciones de la MISMA imagen original.
            tensor_img = transform_pipeline(raw_image)

            # Desnormalizamos para poder visualizar correctamente
            img_array = tensor_img.permute(1, 2, 0).numpy()
            # Asegurarse de usar IMAGENET_STD y IMAGENET_MEAN definidos globalmente (o pasarlos como argumento)
            img_array = img_array * IMAGENET_STD + IMAGENET_MEAN
            img_array = np.clip(img_array, 0, 1)

            # Graficamos en la celda correspondiente de la grilla
            ax = axs[row, col]
            ax.imshow(img_array)

            if col == 0:
                ax.set_title(f"Original/Var 1 (Img {row+1})", fontsize=10, fontweight='bold')
            else:
                ax.set_title(f"Variación {col+1}", fontsize=10)

            ax.axis('off')

    plt.tight_layout()
    plt.show()


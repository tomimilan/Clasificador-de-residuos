"""Para dar cumplimiento a los requerimientos de la tercera semana del pliego del proyecto, avanzamos en la fase de modelado experimental utilizando técnicas de Transferencia de Conocimiento (Transfer Learning).

Tras analizar las alternativas típicas del estado del arte (como ResNet-34 o MobileNetV3), nuestro equipo seleccionó EfficientNet-B0 como el backbone extractor de características principales por los siguientes motivos de ingeniería:

Escalamiento Compuesto (Compound Scaling): A diferencia de las redes tradicionales que escalan de forma aislada una única dimensión del modelo (la profundidad en ResNet, el ancho de canales en WideResNet o la resolución espacial), EfficientNet introduce un método de escalado armónico. Mediante un coeficiente compuesto fijo, escala de forma uniforme la profundidad de capas, el ancho de canales y la resolución de la imagen de entrada. Esto maximiza la captura de características geométricas complejas y texturas con una inversión computacional mínima.

Economía de Parámetros y Mitigación del Overfitting: EfficientNet-B0 cuenta con apenas 5.3 millones de parámetros, logrando una precisión en el dataset ImageNet superior a la de ResNet-50, la cual arrastra más de 25 millones de parámetros. Al tener un dataset compacto (~2.500 imágenes limpias), utilizar un modelo pesado incrementaría drásticamente el riesgo de sobreajuste (overfitting). La esbelteca de EfficientNet actúa como un regularizador implícito.

Viabilidad para Despliegue en Streamlit Cloud (Semana 4): Dado que el modelo final operará en un servidor web gratuito con severas restricciones de memoria RAM y CPU, el peso del archivo de pesos (.pth) y la latencia de inferencia en milisegundos son variables críticas. EfficientNet-B0 garantiza ejecuciones ultra rápidas en entornos embebidos o servidores de baja capacidad, permitiendo que la demo de la cámara web funcione de manera fluida y en tiempo real.

Definición de la Arquitectura y Reemplazo del Clasificador

En esta celda importamos el modelo preentrenado con los pesos oficiales de ImageNet v1, "serruchamos" la cabeza de clasificación original de 1.000 clases y la reemplazamos por una estructura lineal adaptada a nuestros 4 contenedores GIRSU.
"""

import torch
import torch.nn as nn
import torchvision.models as models
from torchvision.models import EfficientNet_B0_Weights

def inicializar_modelo_efficientnet(num_clases=4, entrenar_completo=True):
    """
    Instancia EfficientNet-B0.
    Reemplaza el clasificador final para adaptarlo a la Ley GIRSU Mendoza.
    """
    # 1. Cargar los pesos preentrenados oficiales (ImageNet)
    weights = EfficientNet_B0_Weights.DEFAULT
    model = models.efficientnet_b0(weights=weights)

    # 2. Estrategia de congelamiento (Freezing) para experimentos
    if not entrenar_completo:
        # Modo Feature Extractor: Congelamos todo el backbone
        for param in model.parameters():
            param.requires_grad = False
        print("-> Configuración: Feature Extractor (Backbone Congelado)")
    else:
        # Modo Fine-Tuning End-to-End: Dejamos las capas abiertas a cambios leves
        for param in model.parameters():
            param.requires_grad = True
        print("-> Configuración: Fine-Tuning End-to-End (Backbone Desbloqueado)")

    # 3. Modificar la cabeza del clasificador (Classifier Head)
    # En EfficientNet, el clasificador está dentro de model.classifier en la capa indexada [1]
    in_features = model.classifier[1].in_features

    # Reemplazamos por una capa lineal con salida adaptada a nuestras 4 clases
    model.classifier[1] = nn.Linear(in_features=in_features, out_features=num_clases)

    return model

# Prueba rápida de instanciación
model_test = inicializar_modelo_efficientnet(num_clases=4, entrenar_completo=True)

"""Pérdida Ponderada para Desbalance Urbano e Instanciación

Para evitar que el modelo ignore las clases con menos imágenes (como el tacho Amarillo o el tacho Marrón), calculamos el peso inverso de las clases basándonos en tu set de entrenamiento real de la semana pasada, y configuramos el optimizador AdamW con una tasa de aprendizaje conservadora ($1 \times 10^{-4}$) para evitar la destrucción del conocimiento previo (catastrophic forgetting).
"""

import pandas as pd
import torch.optim as optim

# 1. Detectar hardware (Aprovechar la GPU T4 de Colab)
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"Entrenando en el dispositivo: {device}")

# 2. Leer el CSV de entrenamiento para calcular el desbalance real de tus carpetas
train_df = pd.read_csv("data/train.csv")
conteo_clases = train_df['label'].value_counts().sort_index().values
total_muestras = len(train_df)

# Calcular pesos usando la inversa de la frecuencia para balancear el gradiente
pesos_clases = total_muestras / (len(conteo_clases) * conteo_clases)
pesos_tensor = torch.FloatTensor(pesos_clases).to(device)

print(f"Conteo de muestras por tacho en Train: {conteo_clases}")
print(f"Pesos calculados aplicados a la función de pérdida: {pesos_clases}")

# 3. Instanciar el modelo definitivo en la GPU
model = inicializar_modelo_efficientnet(num_clases=4, entrenar_completo=True)
model = model.to(device)

# 4. Definir criterio de pérdida ponderada y optimizador con Weight Decay
criterion = nn.CrossEntropyLoss(weight=pesos_tensor)
optimizer = optim.AdamW(model.parameters(), lr=1e-4, weight_decay=1e-2)

"""Definición de las Funciones de Entrenamiento y Validación

Para cumplir con la rigurosidad metodológica que exige la cátedra, encapsulamos las rutinas de propagación hacia adelante, cálculo de métricas y retropropagación en funciones independientes por época, utilizando tqdm para seguir de forma visual el progreso del bucle.
"""

from tqdm import tqdm

def train_one_epoch(model, dataloader, criterion, optimizer, device):
    model.train()
    running_loss = 0.0
    correct_predictions = 0
    total_samples = 0

    for images, labels in tqdm(dataloader, desc="🏋️ Entrenando", leave=False):
        images, labels = images.to(device), labels.to(device)

        # Resetear gradientes acumulados
        optimizer.zero_grad()

        # Forward pass
        outputs = model(images)
        loss = criterion(outputs, labels)

        # Backward pass y actualización de pesos
        loss.backward()
        optimizer.step()

        # Estadísticas
        running_loss += loss.item() * images.size(0)
        _, preds = torch.max(outputs, 1)
        correct_predictions += torch.sum(preds == labels.data)
        total_samples += images.size(0)

    epoch_loss = running_loss / total_samples
    epoch_acc = correct_predictions.double() / total_samples
    return epoch_loss, epoch_acc.item()

def validate_one_epoch(model, dataloader, criterion, device):
    model.eval()
    running_loss = 0.0
    correct_predictions = 0
    total_samples = 0

    # Desactivar el cálculo de gradientes para ahorrar memoria y acelerar
    with torch.no_grad():
        for images, labels in tqdm(dataloader, desc="🧪 Validando", leave=False):
            images, labels = images.to(device), labels.to(device)

            outputs = model(images)
            loss = criterion(outputs, labels)

            running_loss += loss.item() * images.size(0)
            _, preds = torch.max(outputs, 1)
            correct_predictions += torch.sum(preds == labels.data)
            total_samples += images.size(0)

    epoch_loss = running_loss / total_samples
    epoch_acc = correct_predictions.double() / total_samples
    return epoch_loss, epoch_acc.item()

"""Bucle de Entrenamiento Principal y Almacenamiento del Modelo

Esta celda ejecuta el entrenamiento durante 10 épocas (tiempo óptimo para EfficientNet con 2.500 imágenes en Colab), monitorea la evolución de las métricas y exporta el archivo binario de pesos .pth directo a tu carpeta de producción para dejar todo listo para el deploy en la Semana 4.
"""

import time

EPOCHS = 10
history = {"train_loss": [], "train_acc": [], "val_loss": [], "val_acc": []}
best_val_acc = 0.0

print("🚀 Iniciando el proceso de optimización del clasificador...")
start_time = time.time()

for epoch in range(EPOCHS):
    # Entrenar y validar
    train_loss, train_acc = train_one_epoch(model, train_loader, criterion, optimizer, device)
    val_loss, val_acc = validate_one_epoch(model, val_loader, criterion, device)

    # Guardar métricas en el historial
    history["train_loss"].append(train_loss)
    history["train_acc"].append(train_acc)
    history["val_loss"].append(val_loss)
    history["val_acc"].append(val_acc)

    print(f"Época [{epoch+1}/{EPOCHS}] -> "
          f"Train Loss: {train_loss:.4f} | Train Acc: {train_acc*100:.2f}% || "
          f"Val Loss: {val_loss:.4f} | Val Acc: {val_acc*100:.2f}%")

    # Guardar los mejores pesos si mejora la precisión en validación
    if val_acc > best_val_acc:
        best_val_acc = val_acc
        os.makedirs("prod", exist_ok=True)
        torch.save(model.state_dict(), "prod/efficientnet_girsu_mendoza.pth")
